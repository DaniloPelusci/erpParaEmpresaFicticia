    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection connection = ConnectionFactory.getConnection();
            ModeloUsuario modeloUsuario = new ModeloUsuario();
            UsuarioDAO usuarioDAO = new UsuarioDAO(connection);
            String senha = new String(jPasswordFieldSenha.getPassword()).trim();
            modeloUsuario.setUsuario(jTextFieUsuario.getText());
            modeloUsuario.setSenha(senha);
            modeloUsuario.setIdFuncionario(Long.parseLong(jTextFieldIDFuncionario.getText()));

            if (jTextFieldUsuario.getText().isEmpty()) {
                if (usuarioDAO.verificacaoUsuario(modeloUsuario)) {
                    JOptionPane.showMessageDialog(null, " usuario j√° existente ");

                } else {
                    usuarioDAO.adcionar(modeloUsuario);
                }

            } else {
                modeloUsuario.setId(Long.parseLong(jTextFieldUsuario.getText()));
                if (usuarioDAO.verificacaoUsuarioExistenteFuncionarioCadastrado(modeloUsuario)) {
                    JOptionPane.showMessageDialog(null, " usuario j√° existente ");

                } else {
                    usuarioDAO.altera(modeloUsuario);
                }
            }
            connection.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }

        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

