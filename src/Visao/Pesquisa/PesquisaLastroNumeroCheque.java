    public void preencherTabela() throws SQLException {// preenchimento da tabela  ,  linhas e colunas , LuizDOURADO
        try (Connection connection = ConnectionFactory.getConnection()) {
            ParcelaDAO parcelaDAO = new ParcelaDAO(connection);
            ModeloBanco modeloBanco = (ModeloBanco) jComboBoxBanco.getSelectedItem();

            jTableVenda.setModel(parcelaDAO.listaTabelaPorNumeroCheque(jTextFieldNumeroGrafica.getText(),modeloBanco.getId()));
            jTableVenda.setRowSorter(new TableRowSorter( parcelaDAO.listaTabelaPorNumeroCheque(jTextFieldNumeroGrafica.getText(),modeloBanco.getId())));
            jTableVenda.getColumnModel().getColumn(0).setPreferredWidth(2);
            jTableVenda.getColumnModel().getColumn(0).setResizable(false);
            jTableVenda.getColumnModel().getColumn(1).setPreferredWidth(20);
            jTableVenda.getColumnModel().getColumn(1).setResizable(false);

            jTableVenda.getTableHeader().setReorderingAllowed(false);
            connection.close();

        }
    }

    private void listarParcelasLocacao() throws SQLException {
        Connection connection = ConnectionFactory.getConnection();
        ParcelaDAO parcelaDAO = new ParcelaDAO(connection);
        ModeloBanco modeloBanco = (ModeloBanco) jComboBoxBanco.getSelectedItem();
        List<ModeloParcela> modeloParcelas = parcelaDAO.listaPorNumeroCheque(jTextFieldNumeroGrafica.getText(), modeloBanco.getId());

        String[] colunas = new String[]{"ID", "Estado", "Data Pagamento", "Data Vencimento", "Valor"};
        ArrayList dados = new ArrayList();
        Date teste;
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");

        for (ModeloParcela v : modeloParcelas) {
            if (v.isEstatosParcela()) {
                dados.add(new Object[]{v.getId(), "Pago", format.format(v.getDataPagamento()), format.format(v.getDataVencimento()), v.getValor()});
            } else if (!v.isEstatosParcela()) {
                dados.add(new Object[]{v.getId(), "Não Pago", "00/00/0000", format.format(v.getDataVencimento().getTime()), v.getValor()});
            }
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableParcelasLocacao.setModel(modelo);
        jTableParcelasLocacao.setRowSorter(new TableRowSorter(modelo));
        jTableParcelasLocacao.getColumnModel().getColumn(0).setPreferredWidth(2);
        jTableParcelasLocacao.getColumnModel().getColumn(0).setResizable(false);
        jTableParcelasLocacao.getColumnModel().getColumn(1).setPreferredWidth(20);
        jTableParcelasLocacao.getColumnModel().getColumn(1).setResizable(false);
        jTableParcelasLocacao.getColumnModel().getColumn(2).setPreferredWidth(20);
        jTableParcelasLocacao.getColumnModel().getColumn(2).setResizable(false);
        jTableParcelasLocacao.getColumnModel().getColumn(3).setPreferredWidth(20);
        jTableParcelasLocacao.getColumnModel().getColumn(3).setResizable(false);
        jTableParcelasLocacao.getColumnModel().getColumn(4).setPreferredWidth(20);
        jTableParcelasLocacao.getColumnModel().getColumn(4).setResizable(false);
        connection.close();

    }

