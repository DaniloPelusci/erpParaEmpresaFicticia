private String validacao() {
        String validar = "";

        int cont = 0;

        if (!logica.LogicasPedido.isfloat(jTextFieldNumeroGrafica.getText())) {
            validar += "Numero de Pedido invalido ";

            cont++;
        }

        return validar;
    }
