    public void preencherTabela() throws SQLException {
        try (Connection connection = ConnectionFactory.getConnection()) {
            PedidoDAO pedidoDAO = new PedidoDAO(connection);

            if (jCheckBox1.isSelected()) {
                jTableClientes1.setModel(pedidoDAO.listaTabelaPorNumeroPedido(jTextFieldNumeroGrafica.getText()));
                jTableClientes1.setRowSorter(new TableRowSorter(pedidoDAO.listaTabelaPorNumeroPedido(jTextFieldNumeroGrafica.getText())));
            } else {
                jTableClientes1.setModel(pedidoDAO.listaTabelaPorNumeroGraficaVenda(jTextFieldNumeroGrafica.getText()));
                jTableClientes1.setRowSorter(new TableRowSorter(pedidoDAO.listaTabelaPorNumeroGraficaVenda(jTextFieldNumeroGrafica.getText())));

            }

            jTableClientes1.getColumnModel().getColumn(0).setPreferredWidth(2);
            jTableClientes1.getColumnModel().getColumn(0).setResizable(false);
            jTableClientes1.getColumnModel().getColumn(1).setPreferredWidth(20);
            jTableClientes1.getColumnModel().getColumn(1).setResizable(false);

            jTableClientes1.getTableHeader().setReorderingAllowed(false);
            connection.close();

        }
    }

