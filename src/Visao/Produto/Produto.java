    private void jTableClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesMouseClicked
        
        String idtabela = "" + jTableClientes.getValueAt(jTableClientes.getSelectedRow(), 0);        // TODO add your handling code here:
        Long id = Long.parseLong(idtabela);
        String nome = "" + jTableClientes.getValueAt(jTableClientes.getSelectedRow(), 1);
        try {
            ModeloProduto modeloProdutoTabela = new ModeloProduto();
            ModeloTipoProduto modeloTipoProdutoTabela = new ModeloTipoProduto();
            ModeloNomenclatura modeloNomenclaturaTabela = new ModeloNomenclatura();
            ModeloModeloProduto modeloModeloProdutoTabela = new ModeloModeloProduto();
            Connection Connection = new ConnectionFactory().getConnection();
            ProdutoDAO produtoDAO = new ProdutoDAO(Connection);
            TipoProdutoDAO tipoProdutoDAO = new TipoProdutoDAO(Connection);
            NomenclaturaDAO nomenclaturaDAO = new NomenclaturaDAO(Connection);
            ModeloProdutoDAO modeloprodutoDAO = new ModeloProdutoDAO(Connection);
            modeloProdutoTabela = produtoDAO.buscaPorId(id);
            modeloTipoProdutoTabela = tipoProdutoDAO.buscaPorId(modeloProdutoTabela.getTipo());
            modeloNomenclaturaTabela = nomenclaturaDAO.buscaPorId(modeloProdutoTabela.getNomenclatura());
            modeloModeloProdutoTabela = modeloprodutoDAO.buscaPorId(modeloProdutoTabela.getModelo());
            for (int i = 0; i < jComboBoxModelo.getItemCount(); ++i) {
                ModeloModeloProduto modeloModeloProduto2 = (ModeloModeloProduto) jComboBoxModelo.getItemAt(i);
                if (modeloModeloProdutoTabela.getId().equals(modeloModeloProduto2.getId())) {
                    jComboBoxModelo.setSelectedIndex(i);
                    break;
                }
            }
            for (int i = 0; i < jComboBoxNomenclatura.getItemCount(); ++i) {
                ModeloNomenclatura modeloNomenclatura2 = (ModeloNomenclatura) jComboBoxNomenclatura.getItemAt(i);
                if (modeloNomenclaturaTabela.getId().equals(modeloNomenclatura2.getId())) {
                    jComboBoxNomenclatura.setSelectedIndex(i);
                    break;
                }
            }
            for (int i = 0; i < jComboBoxTipo.getItemCount(); ++i) {
                ModeloTipoProduto modeloTipoProduto2 = (ModeloTipoProduto) jComboBoxTipo.getItemAt(i);
                if (modeloTipoProdutoTabela.getId().equals(modeloTipoProduto2.getId())) {
                    jComboBoxTipo.setSelectedIndex(i);
                    break;
                }
            }
            jTextFieldID.setText(idtabela);
            jTextFieldNome.setText(nome);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            Logger.getLogger(Produto.class.getName()).log(Level.SEVERE, null, ex);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jTableClientesMouseClicked

