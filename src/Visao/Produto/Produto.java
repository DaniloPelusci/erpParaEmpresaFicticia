    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try (Connection connection = ConnectionFactory.getConnection()) {
            ModeloProduto modeloProduto = new ModeloProduto();
            ProdutoDAO produtoDAO = new ProdutoDAO(connection);
            //tipo
            ModeloTipoProduto modeloTipoProduto = (ModeloTipoProduto) jComboBoxTipo.getSelectedItem();

            modeloProduto.setTipo(modeloTipoProduto.getId());
            //nomenclatura
            ModeloNomenclatura modeloNomenclatura = (ModeloNomenclatura) jComboBoxNomenclatura.getSelectedItem();
            modeloProduto.setNomenclatura(modeloNomenclatura.getId());

            //Modelo
            ModeloModeloProduto modeloModeloProduto = (ModeloModeloProduto) jComboBoxModelo.getSelectedItem();
            modeloProduto.setModelo(modeloModeloProduto.getId());

            modeloProduto.setNome(jTextFieldNome.getText().toUpperCase());

            String idSalvar = jTextFieldID.getText();
            if (idSalvar.equals("")) {
                produtoDAO.adiciona(modeloProduto);

            } else {
                modeloProduto.setId(Long.parseLong(idSalvar));

                produtoDAO.altera(modeloProduto);

            }

            PreencherTabela();
            JOptionPane.showMessageDialog(rootPane, "Dados Inseridos Com Sucesso");

            PreencherTabela();

        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(null, " Falha no Armazenamento de dados \n ERRO :" + ex);
        }
        jTextFieldID.setText("");

        jTextFieldNome.setText("");

    }//GEN-LAST:event_jButtonSalvarActionPerformed

