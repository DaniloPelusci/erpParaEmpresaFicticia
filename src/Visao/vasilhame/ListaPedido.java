public void setRetornoConsultaCliente(String iDCliente, String nomeCliente) {
        this.jTextFieldIDCliente.setText(String.valueOf(iDCliente));
        this.jTextFieldNomeCliente.setText(String.valueOf(nomeCliente));
        Long id = Long.parseLong(jTextFieldIDCliente.getText());
        try {
            PreencherTabela(id);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            Logger.getLogger(ListaPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void PreencherTabela(Long id) throws SQLException {// preenchimento da tabela  ,  linhas e colunas , LuizDOURADO
        try (Connection connection = ConnectionFactory.getConnection()) {
            PedidoDAO pedidoDAO = new PedidoDAO(connection);
            if (id == -1) {
                jTableClientes.setModel(pedidoDAO.listaTabelaPedidosAbertos());
                jTableClientes.setRowSorter(new TableRowSorter(pedidoDAO.listaTabelaPedidosAbertos()));
            } else {
                jTableClientes.setModel(pedidoDAO.listaTabelaPedidosAbertosBuscaPorIDCliente(id));
                jTableClientes.setRowSorter(new TableRowSorter(pedidoDAO.listaTabelaPedidosAbertosBuscaPorIDCliente(id)));
            }
            jTableClientes.getColumnModel().getColumn(0).setPreferredWidth(2);
            jTableClientes.getColumnModel().getColumn(0).setResizable(false);
            jTableClientes.getColumnModel().getColumn(1).setPreferredWidth(20);
            jTableClientes.getColumnModel().getColumn(1).setResizable(false);
            
            jTableClientes.getTableHeader().setReorderingAllowed(false);
            connection.close();
        }
    }
    
