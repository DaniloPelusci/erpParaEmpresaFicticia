                Connection Connection = new ConnectionFactory().getConnection();
                PedidoDAO pedidoDAO = new PedidoDAO(Connection);
                pedidoDAO.removerPedidoProdutoPeloCliente(id);
                pedidoDAO.removeAllPedidosAbertosporIdCLiente(id);
