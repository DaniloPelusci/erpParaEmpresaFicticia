    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        NovoClass novoClass = new NovoClass();
        EmailLogica emailLogica = new EmailLogica();

        try {

            if (this.identifDentro == 0) {
                emailLogica.SalvarNotaFiscal("S:\\notafiscal");

                JOptionPane.showMessageDialog(null, "Enviado com sucesso!!");
            } else if (this.identifDentro == 1) {
                emailLogica.validaNotaParaEnvio("S:\\notafiscal");
                JOptionPane.showMessageDialog(null, "Emails para envio de nota fiscal validados com sucesso! ");

            } else if (this.identifDentro == 2) {
                novoClass.verificarSeTem("S:\\boletos");
                JOptionPane.showMessageDialog(null, "Emails para envio validados com sucesso! ");
            } else if (this.identifDentro == 3) {
                emailLogica.SalvarNotaLocacao("S:\\nt\\");
            }else if (this.identifDentro == 4) {
                novoClass.verificarSeTemLocacao("S:\\boletolocacao\\");
            }

        } catch (SQLException ex) {
            Logger.getLogger(EmailBoletoNotas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);

        } catch (javax.mail.MessagingException ex) {
            Logger.getLogger(EmailBoletoNotas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

