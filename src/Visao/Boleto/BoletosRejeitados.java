    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Connection connection;

        try {
            List<ModeloParcela> parcelas = new ArrayList<ModeloParcela>();
            connection = new ConnectionFactory().getConnection();

            PedidoDAO pedidoDAO = new PedidoDAO(connection);
            BoletoDao boletoDao = new BoletoDao(connection);
            ParcelaDAO parcelaDAO = new ParcelaDAO(connection);
            ModeloPedido modeloPedido = new ModeloPedido();
            ModeloBoleto modeloBoleto = new ModeloBoleto();
            modeloPedido = pedidoDAO.buscaPorVenda(Integer.parseInt(jTextFieldNossoNumero.getText()));

            parcelas = parcelaDAO.listaPorIdVenda(Long.parseLong(jTextFieldNossoNumero.getText()));

            for (ModeloParcela e : parcelas) {
                if (boletoDao.validaBoletoParcela(e.getId()));
                {
                    modeloBoleto = boletoDao.buscaPorId(e.getId());
                    System.out.println(boletoDao.pegarUltimoNumero());
                    if (modeloBoleto.getNossoNumero() == boletoDao.pegarUltimoNumero()) {
                        //boletoDao.alteraPrimeiroBoleto();

                    }

                    System.out.println(Integer.parseInt(String.valueOf(e.getId())));
                    boletoDao.remove(Long.parseLong(String.valueOf(e.getId())));
                    pedidoDAO.alterarBoleto(false, modeloPedido.getId());
                }

            }

            connection.close();

        } catch (SQLException ex) {
            Logger.getLogger(BoletosRejeitados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

