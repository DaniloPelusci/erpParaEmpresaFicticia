            CidadeDAO cidadeDAO = new CidadeDAO(connection);
            ModeloEstado estadoCombo = (ModeloEstado) jComboBoxEstado.getSelectedItem();
            List<ModeloCidades> cidadeCombo = cidadeDAO.buscaIdPorEstado(estadoCombo.getId());
