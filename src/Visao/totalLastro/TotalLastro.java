    public void pesquisaLastro() throws SQLException {
        Connection connection = ConnectionFactory.getConnection();
        VendaDAO vendaDAO = new VendaDAO(connection);
        TipoAcaoDAO tipoAcaoDAO = new TipoAcaoDAO(connection);

        int locacaoPecas = tipoAcaoDAO.somaPecasLocacaoTotal();
        int devolucaopecas = tipoAcaoDAO.somaPecasDevolucaoTotal() + tipoAcaoDAO.somaPecasVendaTotal();

        int locacaoCilindro = tipoAcaoDAO.somaCilindroLocacaoTotal();
        int devolucaoCilindro = tipoAcaoDAO.somaCilindroDevolucaoTotal() + tipoAcaoDAO.somaCilindroVendaTotal();

        int locacaoConcentrador = tipoAcaoDAO.somaConcentradorLocacaoTotal();
        int devolucaoConcentrador = tipoAcaoDAO.somaConcentradorDevolucaoTotal() + tipoAcaoDAO.somaConcentradorVendaTotal();

        int resultConcentrador = locacaoConcentrador - devolucaoConcentrador;
        int resultPecas = locacaoPecas - devolucaopecas;
        int resultCilindros = locacaoCilindro - devolucaoCilindro;
        int totalEmprestado = totalLocado();
        int total = resultCilindros - totalEmprestado ;
        
        jTextFieldTotalLocado.setText(""+total);
        jTextFieldPecas.setText("" + resultPecas);
        jTextFieldVasilhames.setText("" + resultCilindros);
        jTextFieldConcentrador.setText("" + resultConcentrador);
    }


